                    <div class="sub-section">
                        <div class="sub-title pl-0">Data Protection Principles</div>
                        <p class="para-section">
                            There are 8 data protection principles which ensure that organisations collect, store and
                            process personal data in a way that protects the confidentiality of customers. These
                            principles are set out in schedule 1 of the Act and are as follows:
                        </p>
                        <p class="para-section">
                            i. Data must be handled fairly and lawfully<br>We have legitimate grounds to obtain, store
                            and process customers' data, namely, to assist you obtain a passport, visa and for other
                            purposes authorised by the law and contracts between us and our customers. We ensure that
                            your data will not be used for anything unlawful.
                        </p>
                        <p class="para-section">
                            ii. Personal data shall be obtained only for one or more specified and lawful purposes, and
                            shall not be further processed in any manner incompatible with that purpose or those
                            purposes.<br>Due to the nature of our job, the purpose for which you are giving us your
                            personal data is obvious to you and us. We will not use your data for any other purpose
                            incompatible with the original purpose unless you first give us your consent or we are
                            compelled by law to do so.
                        </p>
                        <p class="para-section">
                            iii. Personal data shall be adequate, relevant and not excessive in relation to the purpose
                            or purposes for which they are processed.<br>We will only take information we consider
                            relevant and sufficient to deliver our contract with you.
                        </p>
                        <p class="para-section">
                            iv. Personal data shall be accurate and, where necessary, kept up to date.<br>We ensure that
                            the personal data you give us is correct and not misleading. We achieve this by giving you
                            the opportunity to confirm the information you give us and also by requesting documentary
                            proof when necessary. If there are relevant changes in your personal data, eg, change of
                            name or nationality, please let us know as soon as possible so that we can update your
                            record.
                        </p>
                        <p class="para-section">
                            v. Personal data processed for any purpose or purposes shall not be kept for longer than is
                            necessary for that purpose or those purposes.<br>We will, from time to time, review our data
                            base to see if some personal data are still needed to be stored by us. If the purpose for
                            which they were stored has been achieved and we consider that it is no longer necessary to
                            continue storing them, we will securely delete them.
                        </p>
                        <p class="para-section">
                            vi. Personal data shall be processed in accordance with the rights of data subjects under
                            this Act.<br>We will process your personal data in such a way that it will enable you to do
                            any of the following:
                        </p>
                        <ul class="para-list-digit">
                            <li>
                                a right of access to a copy of your personal data, popularly known as 'subject access
                                request'. The current fee for that is £10 per standard request and we aim to provide
                                this within 14 days. Subject Access Request should be made to info@oisservices.com by
                                email or by post The Head of Centre, OIS Services, 56-57 Fleet street, EC4Y 1JU, London
                            </li>
                            <li>
                                a right to object to processing your personal data in a way that is likely to cause
                                damage or distress to you;
                            </li>
                            <li>
                                a right to prevent processing your personal data in a way that is incompatible to the
                                original purpose or purposes;
                            </li>
                            <li>
                                a right to object to decisions being taken by automated means;
                            </li>
                            <li>
                                a right, when appropriate, to have inaccurate personal data rectified, blocked or
                                deleted, and
                            </li>
                            <li>
                                a right to claim compensation for damages caused by a breach of the Act.
                            </li>
                        </ul>
                        <p class="para-section">
                            vii. Personal data security<br>Your personal data are held in a very secure environment both
                            physically and technically. The areas where personal data are kept are out of bounds for all
                            non-authorised persons. Access to these parts of the premises can only be gained by
                            electronic key issued only to authorised persons. Your personal data can only be accessed on
                            the system by authorised persons. Only such authorised persons can alter, disclose or
                            destroy your personal data. Further, such authorised persons can only carry out any of those
                            functions in the course of their lawful duties. Therefore, the chances of your personal data
                            being unlawfully processed or accidentally damaged, destroyed or lost are very minimal.
                            Further, it is our policy to ensure that:
                        </p>
                        <ul class="para-list-digit">
                            <li>
                                each authorised employee who has access to personal data has his own password which is
                                not shared with other persons,
                            </li>
                            <li>
                                approved security software and firewall are used to protect our data,
                            </li>
                            <li>
                                printed personal data not in use are shredded,
                            </li>
                            <li>
                                we discourage personal data being stored on laptops and other mobile devices and media
                                but if they do, should be locked securely and not taken out of the office.
                            </li>
                        </ul>
                        <p class="para-section">
                            viii. Transfer of personal data outside the European Economic Area<br>Your personal data
                            will not be transferred to a country or territory outside the European Economic Area unless
                            that country or territory ensures an adequate level of protection for the rights and
                            freedoms of data subjects in relation to the processing of personal data. There are only few
                            counties outside the EEA that have been accepted as having adequate levels of protection for
                            the processing of persona data comparable to the EEA countries. When we are transferring
                            personal data to the USA, we ensure that the recipient is a signatory to the US Department
                            of Commerce Safe Harbour Scheme. This scheme is recognised by the European Commission as
                            providing adequate protection for data subjects. In relation to other countries outside the
                            EEA that have not been accepted as having equivalent security and safeguards in place for
                            processing personal data, we either sign a separate contract, the terms of which are
                            approved by the European Directives or ensure that there is a clause in the contract that
                            protects the processing of personal data of all our clients.
