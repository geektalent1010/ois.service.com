                        <div class="sub-title pl-0">2.5 Lawfulness of Processing</div>
                        <p class="para-section">
                            There are six alternative ways in which the lawfulness of a specific case of processing of
                            personal data may be established under the GDPR. It is OIS policy to identify the
                            appropriate basis for processing and to document it, in accordance with the Regulation. The
                            options are described in brief in the following sections.
                        </p>
                        <div class="sub-title pl-0">2.5.1 Consent</div>
                        <p class="para-section">
                            Unless it is necessary for a reason allowable in the GDPR, OIS will always obtain explicit
                            consent from a data subject to collect and process their data. In case of children below the
                            age of 16 (a lower age may be allowable in specific EU member states) parental consent will
                            be obtained. Transparent information about our usage of their personal data will be provided
                            to data subjects at the time that consent is obtained and their rights with regard to their
                            data explained, such as the right to withdraw consent. This information will be provided in
                            an accessible form, written in clear language and free of charge. If the personal data are
                            not obtained directly from the data subject then this information will be provided to the
                            data subject within a reasonable period after the data are obtained and definitely within
                            one month.
                        </p>
                        <div class="sub-title pl-0">2.5.2 Performance of a Contract</div>
                        <p class="para-section">
                            Where the personal data collected and processed are required to fulfil a contract with the
                            data subject, explicit consent is not required. This will often be the case where the
                            contract cannot be completed without the personal data in question e.g. a delivery cannot be
                            made without an address to deliver to.
                        </p>
                        <div class="sub-title pl-0">2.5.3 Legal Obligation</div>
                        <p class="para-section">
                            If the personal data is required to be collected and processed in order to comply with the
                            law, then explicit consent is not required. This may be the case for some data related to
                            employment and taxation for example, and for many areas addressed by the public sector.
                        </p>
                        <div class="sub-title pl-0">2.5.4 Vital Interests of the Data Subject</div>
                        <p class="para-section">
                            In a case where the personal data are required to protect the vital interests of the data
                            subject or of another natural person, then this may be used as the lawful basis of the
                            processing. OIS will retain reasonable, documented evidence that this is the case, whenever
                            this reason is used as the lawful basis of the processing of personal data. As an example,
                            this may be used in aspects of social care, particularly in the public sector.
                        </p>
                        <div class="sub-title pl-0">2.5.5 Task Carried Out in the Public Interest</div>
                        <p class="para-section">
                            Where OIS needs to perform a task that it believes is in the public interest or as part of
                            an official duty then the data subject's consent will not be requested. The assessment of
                            the public interest or official duty will be documented and made available as evidence where
                            required.
                        </p>
                        <div class="sub-title pl-0">2.5.6 Legitimate Interests</div>
                        <p class="para-section">
                            If the processing of specific personal data is in the legitimate interests of OIS and is
                            judged not to affect the rights and freedoms of the data subject in a significant way, then
                            this may be defined as the lawful reason for the processing. Again, the reasoning behind
                            this view will be documented.
                        </p>
                        <div class="sub-title pl-0">2.6 Privacy by Design</div>
                        <p class="para-section">
                            OIS has adopted the principle of privacy by design and will ensure that the definition and
                            planning of all new or significantly changed systems that collect or process personal data
                            will be subject to due consideration of privacy issues, including the completion of one or
                            more data protection impact assessments.
                        </p>
                        <p class="para-section">
                            The data protection impact assessment will include:
                        </p>
                        <p class="para-section">
                            Consideration of how personal data will be processed and for what purposes<br>Assessment of
                            whether the proposed processing of personal data is both necessary and proportionate to the
                            purpose(s)
                        </p>
                        <p class="para-section">
                            Assessment of the risks to individuals in processing the personal data<br>What controls are
                            necessary to address the identified risks and demonstrate compliance with legislation<br>Use
                            of techniques such as data minimization and pseudonymisation will be considered where
                            applicable and appropriate.
                        </p>
                        <div class="sub-title pl-0">2.7 Contracts Involving the Processing of Personal Data</div>
                        <p class="para-section">
                            OIS will ensure that all relationships it enters into that involve the processing of
                            personal data are subject to a documented contract that includes the specific information
                            and terms required by the GDPR. For more information, see the GDPR Controller-Processor
                            Agreement Policy
                        </p>
                        <div class="sub-title pl-0">2.8 International Transfers of Personal Data</div>
                        <p class="para-section">
                            Transfers of personal data outside the European Union will be carefully reviewed prior to
                            the transfer taking place to ensure that they fall within the limits imposed by the GDPR.
                            This depends partly on the European Commission's judgement as to the adequacy of the
                            safeguards for personal data applicable in the receiving country and this may change over
                            time.<br>Intra-group international data transfers will be subject to legally binding
                            agreements referred to as Binding Corporate Rules (BCR) which provide enforceable rights for
                            data subjects.
                        </p>
