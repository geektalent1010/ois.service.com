                        <div class="sub-title pl-0">2.1 The EU General Data Protection Regulation - GDPR</div>
                        <p class="para-section">
                            The General Data Protection Regulation 2016 (GDPR) is one of the most significant pieces of
                            legislation affecting the way that OIS carries out its information processing activities.
                            Significant fines are applicable if a breach is deemed to have occurred under the GDPR,
                            which is designed to protect the personal data of citizens of the European Union. It is
                            OIS's policy to ensure that our compliance with the GDPR and other relevant legislation is
                            clear and demonstrable at all times.
                        </p>
                        <div class="sub-title pl-0">2.2 Definitions</div>
                        <p class="para-section">
                            There are a total of 26 definitions listed within the GDPR and it is not appropriate to
                            reproduce them all here. However, the most fundamental definitions with respect to this
                            policy are as follows:
                        </p>
                        <p class="para-section">
                            Personal data is defined as:
                        </p>
                        <p class="para-section">
                            any information relating to an identified or identifiable natural person ('data subject');
                            an identifiable natural person is one who can be identified, directly or indirectly, in
                            particular by reference to an identifier such as a name, an identification number, location
                            data, an online identifier or to one or more factors specific to the physical,
                            physiological, genetic, mental, economic, cultural or social identity of that natural
                            person;
                        </p>
                        <p class="para-section">
                            'processing' means
                        </p>
                        <p class="para-section">
                            any operation or set of operations which is performed on personal data or on sets of
                            personal data, whether or not by automated means, such as collection, recording,
                            organisation, structuring, storage, adaptation or alteration, retrieval, consultation, use,
                            disclosure by transmission, dissemination or otherwise making available, alignment or
                            combination, restriction, erasure or destruction;
                        </p>
                        <p class="para-section">
                            'controller' means
                        </p>
                        <p class="para-section">
                            the natural or legal person, public authority, agency or other body which, alone or jointly
                            with others, determines the purposes and means of the processing of personal data; where the
                            purposes and means of such processing are determined by Union or Member State law, the
                            controller or the specific criteria for its nomination may be provided for by Union or
                            Member State law;
                        </p>
                        <div class="sub-title pl-0">2.3 Principles Relating to Processing of Personal Data</div>
                        <p class="para-section">
                            There are a number of fundamental principles upon which the GDPR is based.<br>
                            These are as follows:
                        </p>
                        <p class="para-section">
                            1. Personal data shall be:
                        </p>
                        <p class="para-section">
                            (a) processed lawfully, fairly and in a transparent manner in relation to the data subject
                            ('lawfulness, fairness and transparency');
                        </p>
                        <p class="para-section">
                            (b) collected for specified, explicit and legitimate purposes and not further processed in a
                            manner that is incompatible with those purposes; further processing for archiving purposes
                            in the public interest, scientific or historical research purposes or statistical purposes
                            shall, in accordance with Article 89(1), not be considered to be incompatible with the
                            initial purposes ('purpose limitation');
                        </p>
                        <p class="para-section">
                            (c) adequate, relevant and limited to what is necessary in relation to the purposes for
                            which they are processed ('data minimisation');
                        </p>
                        <p class="para-section">
                            (d) accurate and, where necessary, kept up to date; every reasonable step must be taken to
                            ensure that personal data that are inaccurate, having regard to the purposes for which they
                            are processed, are erased or rectified without delay ('accuracy');
                        </p>
                        <p class="para-section">
                            (e) kept in a form which permits identification of data subjects for no longer than is
                            necessary for the purposes for which the personal data are processed; personal data may be
                            stored for longer periods insofar as the personal data will be processed solely for
                            archiving purposes in the public interest, scientific or historical research purposes or
                            statistical purposes in accordance with Article 89(1) subject to implementation of the
                            appropriate technical and organisational measures required by this Regulation in order to
                            safeguard the rights and freedoms of the data subject ('storage limitation');
                        </p>
                        <p class="para-section">
                            (f) processed in a manner that ensures appropriate security of the personal data, including
                            protection against unauthorised or unlawful processing and against accidental loss,
                            destruction or damage, using appropriate technical or organisational measures ('integrity
                            and confidentiality').
                        </p>
                        <p class="para-section">
                            (f) processed in a manner that ensures appropriate security of the personal data, including
                            protection against unauthorised or unlawful processing and against accidental loss,
                            destruction or damage, using appropriate technical or organisational measures ('integrity
                            and confidentiality').
                        </p>
                        <p class="para-section">
                            OIS will ensure that it complies with all of these principles both in the processing it
                            currently carries out and as part of the introduction of new methods of processing such as
                            new IT systems.
                        </p>
