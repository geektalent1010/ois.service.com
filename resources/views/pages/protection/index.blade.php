                        <div class="sub-title pl-0">2.10 Breach Notification</div>
                        <p class="para-section">
                            It is OIS's policy to be fair and proportionate when considering the actions to be taken to
                            inform affected parties regarding breaches of personal data. In line with the GDPR, where a
                            breach is known to have occurred which is likely to result in a risk to the rights and
                            freedoms of individuals, the relevant supervisory authority will be informed within 72
                            hours. This will be managed in accordance with our Information Security Incident Response
                            Procedure which sets out the overall process of handling information security incidents.
                        </p>
                        <p class="para-section">
                            Under the GDPR the relevant DPA has the authority to impose a range of fines of up to four
                            percent of annual worldwide turnover or twenty million Euros, whichever is the higher, for
                            infringements of the regulations.
